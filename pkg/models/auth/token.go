package auth

import (
	"fmt"
	"github.com/dgrijalva/jwt-go"
	"k8s.io/klog"
	"kubesphere.io/kubesphere/pkg/apiserver/authentication/oauth"
	authoptions "kubesphere.io/kubesphere/pkg/apiserver/authentication/options"
	"kubesphere.io/kubesphere/pkg/apiserver/authentication/token"
	"kubesphere.io/kubesphere/pkg/simple/client/cache"
	"time"
)

// The authorization code generated by the authorization server.  The authorization code MUST expire
// shortly after it is issued to mitigate the risk of leaks.
// A maximum authorization code lifetime of 10 minutes is RECOMMENDED.
const authorizationCodeLifetime = 5 * time.Minute
const idTokenLifetime = 5 * time.Minute

type TokenManagementInterface interface {
	// Verify verifies a token, and return a User if it's a valid token, otherwise return error
	Verify(token string) (*token.Claims, error)
	// IssueTo issue an access token and refresh token to the specified user, return error if issuing process failed
	IssueTo(claims *token.Claims) (*oauth.Token, error)
	// IssueCodeTo issue an authorization code to the specified user, return error if issuing process failed
	IssueCodeTo(claims *token.Claims) (string, error)
	// IssueIDTokenTo issue an id token to the specified user, return error if issuing process failed
	IssueIDTokenTo(claims *token.Claims, clientSecret string) (string, error)
}

type tokenOperator struct {
	issuer  token.Issuer
	options *authoptions.AuthenticationOptions
	cache   cache.Interface
}

func NewTokenOperator(cache cache.Interface, options *authoptions.AuthenticationOptions) TokenManagementInterface {
	operator := &tokenOperator{
		issuer:  token.NewTokenIssuer(options.IssuerName, options.JwtSecret, options.MaximumClockSkew),
		options: options,
		cache:   cache,
	}
	return operator
}

func (t tokenOperator) Verify(tokenStr string) (*token.Claims, error) {
	claims, err := t.issuer.Verify(tokenStr, "")
	if err != nil {
		klog.Error(err)
		return nil, err
	}
	// The token is not cached in the member cluster, disable token verification by setting AccessTokenMaxAge to 0.
	if t.options.OAuthOptions.AccessTokenMaxAge == 0 ||
		claims.TokenType == token.StaticToken {
		return claims, nil
	}
	if err := t.tokenCacheValidate(claims.Username, tokenStr); err != nil {
		klog.Error(err)
		return nil, err
	}
	// The client MUST NOT use the authorization code or refresh token more than once.
	// TODO May fail to create user.
	if claims.TokenType == token.RefreshToken || claims.TokenType == token.AuthorizationCode {
		t.revokeUserToken(claims.Username, tokenStr)
	}
	return claims, nil
}

func (t tokenOperator) IssueCodeTo(claims *token.Claims) (string, error) {
	// The authorization code MUST expire shortly after it is issued to mitigate the risk of leaks.
	// https://tools.ietf.org/html/rfc6749#section-4.1.2
	code, err := t.issuer.IssueTo(claims, authorizationCodeLifetime, "")
	if err != nil {
		klog.Error(err)
		return "", err
	}
	if err = t.cacheToken(claims.Username, code, authorizationCodeLifetime); err != nil {
		klog.Error(err)
		return "", err
	}
	return code, nil
}

func (t tokenOperator) IssueIDTokenTo(claims *token.Claims, clientSecret string) (string, error) {
	// If the JWT alg Header Parameter uses a MAC based algorithm such as HS256, HS384, or HS512,
	// the octets of the UTF-8 representation of the client_secret corresponding to the client_id
	// contained in the aud (audience) Claim are used as the key to validate the signature.
	// For MAC based algorithms, the behavior is unspecified if the aud is multi-valued or if
	// an azp value is present that is different than the aud value.
	// https://openid.net/specs/openid-connect-core-1_0.html#IDTokenValidation
	//
	// Expiration time on or after which the ID Token MUST NOT be accepted for processing.
	// The processing of this parameter requires that the current date/time MUST be before the expiration date/time listed in the value.
	// Implementers MAY provide for some small leeway, usually no more than a few minutes,
	// to account for clock skew. Its value is a JSON number representing the number of seconds
	// from 1970-01-01T0:0:0Z as measured in UTC until the date/time.
	// See RFC 3339 [RFC3339] for details regarding date/times in general and UTC in particular.
	// https://openid.net/specs/openid-connect-core-1_0.html#IDToken
	idToken, err := t.issuer.IssueTo(claims, idTokenLifetime, clientSecret)
	if err != nil {
		klog.Error(err)
		return "", err
	}

	return idToken, err
}

func (t tokenOperator) IssueTo(claims *token.Claims) (*oauth.Token, error) {
	accessTokenExpiresIn := t.options.OAuthOptions.AccessTokenMaxAge
	refreshTokenExpiresIn := accessTokenExpiresIn + t.options.OAuthOptions.AccessTokenInactivityTimeout
	copied := token.Claims{
		Username: claims.Username,
		Groups:   claims.Groups,
		Extra:    claims.Extra,
		StandardClaims: jwt.StandardClaims{
			Audience: claims.Audience,
		},
	}

	accessTokenClaims := copied
	accessTokenClaims.TokenType = token.AccessToken
	accessToken, err := t.issuer.IssueTo(&accessTokenClaims, accessTokenExpiresIn, "")
	if err != nil {
		klog.Error(err)
		return nil, err
	}

	refreshTokenClaims := copied
	refreshTokenClaims.TokenType = token.RefreshToken
	refreshToken, err := t.issuer.IssueTo(&refreshTokenClaims, refreshTokenExpiresIn, "")
	if err != nil {
		klog.Error(err)
		return nil, err
	}

	result := &oauth.Token{
		AccessToken:  accessToken,
		TokenType:    "Bearer",
		RefreshToken: refreshToken,
		ExpiresIn:    int(accessTokenExpiresIn.Seconds()),
	}

	if !t.options.MultipleLogin {
		if err = t.revokeAllUserTokens(claims.Username); err != nil {
			klog.Error(err)
			return nil, err
		}
	}

	if accessTokenExpiresIn > 0 {
		if err = t.cacheToken(claims.Username, accessToken, accessTokenExpiresIn); err != nil {
			klog.Error(err)
			return nil, err
		}
		if err = t.cacheToken(claims.Username, refreshToken, refreshTokenExpiresIn); err != nil {
			klog.Error(err)
			return nil, err
		}
	}

	return result, nil
}

func (t tokenOperator) revokeAllUserTokens(username string) error {
	pattern := fmt.Sprintf("kubesphere:user:%s:token:*", username)
	if keys, err := t.cache.Keys(pattern); err != nil {
		klog.Error(err)
		return err
	} else if len(keys) > 0 {
		if err := t.cache.Del(keys...); err != nil {
			klog.Error(err)
			return err
		}
	}
	return nil
}

func (t tokenOperator) revokeUserToken(username, token string) error {
	key := fmt.Sprintf("kubesphere:user:%s:token:%s", username, token)
	if err := t.cache.Del(key); err != nil {
		klog.Error(err)
		return err
	}
	return nil
}

func (t tokenOperator) tokenCacheValidate(username, token string) error {
	key := fmt.Sprintf("kubesphere:user:%s:token:%s", username, token)
	if exist, err := t.cache.Exists(key); err != nil {
		return err
	} else if !exist {
		return fmt.Errorf("token not found in cache")
	}
	return nil
}

func (t tokenOperator) cacheToken(username, token string, duration time.Duration) error {
	key := fmt.Sprintf("kubesphere:user:%s:token:%s", username, token)
	if err := t.cache.Set(key, token, duration); err != nil {
		klog.Error(err)
		return err
	}
	return nil
}
